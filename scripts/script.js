const listContainer = document.querySelector('#card')
const search = document.querySelector('#searchInput');
const searchId = document.querySelector('#searchIdInput');


function searchInKeyUp(event){
  const searched = event.target.value;

  const hinosFound = hinosFilterInSearchByName(searched);

  hinosFound.length > 0 
  ? render(hinosFound)
  : (listContainer.innerHTML = 'Nenhum hino encontrado');
}


function hinosFilterInSearchByName(searched){
  return hinos.filter(hinos => {
      return hinos.titulo.toUpperCase().includes(searched.toUpperCase());
  })
}

search.addEventListener('keyup', _.debounce(searchInKeyUp, 400));


function render(hinos){
    let list = '';

    if(hinos.length <= 0){
        list += `<div>Nenhum hino encontrado</div>`;
    }else{
        hinos.forEach((hino, index) => {
            list += `
              <iframe 
                  width="380" 
                  height="215" 
                  src=${hino.url} 
                  title=${hino.titulo}
                  frameborder="0" 
                  allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" 
                  allowfullscreen>
              </iframe>`;
        });
    }

    listContainer.innerHTML = list;
}